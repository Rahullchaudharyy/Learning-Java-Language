public class Literal {

    public static void main(String a[]) {
        // Literal || Binary
        int num1 = 0b101;
        System.out.println(num1);

        // Literal || Hexa
        int hexa = 0x7E;
        System.out.println(hexa);

        // Literal || Zero Factors : -
        int Num1 = 10_00_00_000;
        System.out.println(Num1);

        // Literal || Char
        char alpha = 'y';
        alpha++;
        System.out.println(alpha);

    }
}
